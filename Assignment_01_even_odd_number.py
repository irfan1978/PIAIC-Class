# Take number as input from the user and tell it is an EVEN or OD number
# RULE: IF remainder is ZERO then it is an EVEN number, If remainder is not ZERO then it is an ODD number

_input_value = input('Enter any number here, we shall let you know it is an EVEN or ODD number : ')
result = (int(_input_value) % 2)
if result == 0:
    print('The number you enter is an EVEN Number.')
else:
    print('The number you enter is an ODD Number.')
